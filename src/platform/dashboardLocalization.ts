export const interpolateDashboardLabel = (
  template: string,
  values: Record<string, string | number>
): string =>
  Object.entries(values).reduce(
    (label, [key, value]) => label.replace(new RegExp(`\\{${key}\\}`, 'g'), String(value)),
    template
  );

export const getDashboardLocaleKey = (
  preferredLanguage: PreferredLanguage | null | undefined
): PreferredLanguage =>
  typeof preferredLanguage === 'string' &&
  preferredLanguageValues.includes(preferredLanguage as PreferredLanguage)
    ? (preferredLanguage as PreferredLanguage)
    : 'english';

export const getDashboardUiCopyForLanguage = (
  preferredLanguage: PreferredLanguage | null | undefined,
  dashboardUiCopyByLanguage: Record<PreferredLanguage, DashboardUiCopy>
): DashboardUiCopy => {
  const language = getDashboardLocaleKey(preferredLanguage);
  const baseCopy = dashboardUiCopyByLanguage[language];

  return {
    ...baseCopy,
    calendar: {
      ...baseCopy.calendar,
      ...DASHBOARD_CALENDAR_TEXT_BY_LANGUAGE[language]
    }
  };
};

export const getDashboardContentForLanguage = (
  preferredLanguage: PreferredLanguage | null | undefined
): DashboardLanguageContent => DASHBOARD_CONTENT_BY_LANGUAGE[getDashboardLocaleKey(preferredLanguage)];
