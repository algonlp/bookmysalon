const rowToRecipient = (row: Row): CampaignRecipientRecord => ({
  id: asText(row.id),
  campaignId: asText(row.campaign_id),
  businessId: asText(row.business_id),
  origin: asText(row.origin) as CampaignRecipientOrigin,
  customerProfileId: asNullableText(row.customer_profile_id),
  customerName: asText(row.customer_name),
  customerPhone: asText(row.customer_phone),
  customerEmail: asText(row.customer_email),
  dedupeKey: asText(row.dedupe_key),
  smsStatus: asText(row.sms_status) as CampaignDispatchStatus,
  smsReason: asText(row.sms_reason),
  smsMessageId: asText(row.sms_message_id),
  emailStatus: asText(row.email_status) as CampaignDispatchStatus,
  emailReason: asText(row.email_reason),
  convertedAppointmentId: asNullableText(row.converted_appointment_id),
  convertedAt: asNullableText(row.converted_at),
  createdAt: asTimestamp(row.created_at),
  updatedAt: asTimestamp(row.updated_at)
});

const templateToRow = (template: CampaignTemplateRecord): Row => ({
  id: template.id,
  business_id: template.businessId,
  template_type: template.templateType,
  sms_body: template.smsBody,
  email_subject: template.emailSubject,
  email_body_text: template.emailBodyText,
  default_discount_percent: template.defaultDiscountPercent ?? null,
  updated_at: template.updatedAt
});

const rowToTemplate = (row: Row): CampaignTemplateRecord => ({
  id: asText(row.id),
  businessId: asText(row.business_id),
  templateType: asText(row.template_type) as CampaignTemplateType,
  smsBody: asText(row.sms_body),
  emailSubject: asText(row.email_subject),
  emailBodyText: asText(row.email_body_text),
  defaultDiscountPercent: asNullableNumber(row.default_discount_percent),
  updatedAt: asTimestamp(row.updated_at)
});

export const marketingRepository = {
  async createCampaign(campaign: CampaignRecord): Promise<CampaignRecord> {
    const client = getSupabaseClient();
    const { error } = await client.from('marketing_campaigns').insert(campaignToRow(campaign));

    if (error) {
      throw new Error(`Failed to create campaign: ${error.message}`);
    }

    return campaign;
  },

  async updateCampaign(campaign: CampaignRecord): Promise<CampaignRecord> {
    const client = getSupabaseClient();
    const { error } = await client
      .from('marketing_campaigns')
      .update(campaignToRow(campaign))
      .eq('id', campaign.id);

    if (error) {
      throw new Error(`Failed to update campaign ${campaign.id}: ${error.message}`);
    }

    return campaign;
  },

  async incrementLinkOpens(campaignId: string): Promise<void> {
    const client = getSupabaseClient();
    const { data, error: readError } = await client
      .from('marketing_campaigns')
      .select('link_opens_count')
      .eq('id', campaignId)
      .maybeSingle();

    if (readError) {
      throw new Error(`Failed to read link opens for campaign ${campaignId}: ${readError.message}`);
    }

    if (!data) {
      return;
    }

    const nextCount = asNumber(data.link_opens_count) + 1;
    const { error: writeError } = await client
      .from('marketing_campaigns')
      .update({ link_opens_count: nextCount, updated_at: new Date().toISOString() })
      .eq('id', campaignId);

    if (writeError) {
      throw new Error(`Failed to increment link opens for campaign ${campaignId}: ${writeError.message}`);
    }
  },

  async getCampaignById(businessId: string, campaignId: string): Promise<CampaignRecord | undefined> {
    const client = getSupabaseClient();
    const { data, error } = await client
      .from('marketing_campaigns')
      .select('*')
      .eq('business_id', businessId)
      .eq('id', campaignId)
      .maybeSingle();

    if (error) {
      throw new Error(`Failed to load campaign ${campaignId}: ${error.message}`);
    }

    return data ? rowToCampaign(data as Row) : undefined;
  },

  async listCampaignsByBusinessId(businessId: string): Promise<CampaignRecord[]> {
    const client = getSupabaseClient();
    const { data, error } = await client
      .from('marketing_campaigns')
      .select('*')
      .eq('business_id', businessId)
      .order('created_at', { ascending: false });

    if (error) {
      throw new Error(`Failed to list campaigns for ${businessId}: ${error.message}`);
    }

    return (data ?? []).map((row) => rowToCampaign(row as Row));
  },

  async insertRecipients(recipients: CampaignRecipientRecord[]): Promise<void> {
    if (recipients.length === 0) {
      return;
    }

    const client = getSupabaseClient();
    const { error } = await client
      .from('marketing_campaign_recipients')
      .upsert(recipients.map(recipientToRow), { onConflict: 'campaign_id,dedupe_key' });

    if (error) {
      throw new Error(`Failed to insert campaign recipients: ${error.message}`);
    }
  },

  async updateRecipient(recipient: CampaignRecipientRecord): Promise<void> {
    const client = getSupabaseClient();
    const { error } = await client
      .from('marketing_campaign_recipients')
      .update(recipientToRow(recipient))
      .eq('id', recipient.id);

    if (error) {
      throw new Error(`Failed to update campaign recipient ${recipient.id}: ${error.message}`);
    }
  },

  async listRecipientsByCampaignId(campaignId: string): Promise<CampaignRecipientRecord[]> {
    const client = getSupabaseClient();
    const { data, error } = await client
      .from('marketing_campaign_recipients')
      .select('*')
      .eq('campaign_id', campaignId)
      .order('created_at', { ascending: true });

    if (error) {
      throw new Error(`Failed to list recipients for campaign ${campaignId}: ${error.message}`);
    }

    return (data ?? []).map((row) => rowToRecipient(row as Row));
  },

  async findRecipientsByBusinessAndContact(
    businessId: string,
    campaignId: string,
    phone: string,
    email: string
  ): Promise<CampaignRecipientRecord[]> {
    const client = getSupabaseClient();
    let query = client
      .from('marketing_campaign_recipients')
      .select('*')
      .eq('business_id', businessId)
      .eq('campaign_id', campaignId);

    if (phone && email) {
      query = query.or(`customer_phone.eq.${phone},customer_email.eq.${email}`);
    } else if (phone) {
      query = query.eq('customer_phone', phone);
    } else if (email) {
      query = query.eq('customer_email', email);
    } else {
      return [];
    }

    const { data, error } = await query;

    if (error) {
      throw new Error(`Failed to find recipients for conversion match: ${error.message}`);
    }

    return (data ?? []).map((row) => rowToRecipient(row as Row));
  },

  async getTemplate(
    businessId: string,
    templateType: CampaignTemplateType
  ): Promise<CampaignTemplateRecord | undefined> {
    const client = getSupabaseClient();
    const { data, error } = await client
      .from('marketing_campaign_templates')
      .select('*')
      .eq('business_id', businessId)
      .eq('template_type', templateType)
      .maybeSingle();

    if (error) {
      throw new Error(`Failed to load template ${templateType} for ${businessId}: ${error.message}`);
    }

    return data ? rowToTemplate(data as Row) : undefined;
  },

  async upsertTemplate(template: CampaignTemplateRecord): Promise<CampaignTemplateRecord> {
    const client = getSupabaseClient();
    const { error } = await client
      .from('marketing_campaign_templates')
      .upsert(templateToRow(template), { onConflict: 'business_id,template_type' });

    if (error) {
      throw new Error(`Failed to save template ${template.templateType}: ${error.message}`);
    }

    return template;
  }
};
