import { getSupabaseClient } from '../shared/supabase/client';
import type {
  CampaignChannel,
  CampaignDispatchStatus,
  CampaignRecipientOrigin,
  CampaignRecipientRecord,
  CampaignRecipientSource,
  CampaignRecord,
  CampaignStatus,
  CampaignTemplateRecord,
  CampaignTemplateType
} from './marketing.types';

type Row = Record<string, unknown>;

const asText = (value: unknown): string => (typeof value === 'string' ? value.trim() : '');

const asNullableText = (value: unknown): string | undefined => {
  const text = asText(value);
  return text ? text : undefined;
};

const asNumber = (value: unknown, fallback = 0): number =>
  typeof value === 'number' && Number.isFinite(value) ? value : fallback;

const asNullableNumber = (value: unknown): number | undefined =>
  typeof value === 'number' && Number.isFinite(value) ? value : undefined;

const asTimestamp = (value: unknown): string => {
  const text = asNullableText(value);
  return text ?? new Date().toISOString();
};

const campaignToRow = (campaign: CampaignRecord): Row => ({
  id: campaign.id,
  business_id: campaign.businessId,
  name: campaign.name,
  template_type: campaign.templateType,
  discount_percent: campaign.discountPercent ?? null,
  discount_amount_cents: campaign.discountAmountCents ?? null,
  currency_code: campaign.currencyCode,
  target_service_id: campaign.targetServiceId ?? null,
  target_service_name: campaign.targetServiceName,
  free_service_id: campaign.freeServiceId ?? null,
  free_service_name: campaign.freeServiceName,
  happy_hour_start_time: campaign.happyHourStartTime ?? null,
  happy_hour_end_time: campaign.happyHourEndTime ?? null,
  offer_name: campaign.offerName ?? '',
  original_price_cents: campaign.originalPriceCents ?? null,
  discounted_price_cents: campaign.discountedPriceCents ?? null,
  fill_slot_date: campaign.fillSlotDate ?? null,
  fill_slot_time: campaign.fillSlotTime ?? null,
  is_auto_generated: campaign.isAutoGenerated,
  sms_body: campaign.smsBody,
  email_subject: campaign.emailSubject,
  email_body_text: campaign.emailBodyText,
  channel: campaign.channel,
  recipient_source: campaign.recipientSource,
  status: campaign.status,
  recipients_total: campaign.recipientsTotal,
  recipients_sent: campaign.recipientsSent,
  recipients_failed: campaign.recipientsFailed,
  recipients_skipped: campaign.recipientsSkipped,
  booking_link: campaign.bookingLink,
  created_at: campaign.createdAt,
  updated_at: campaign.updatedAt,
  sent_at: campaign.sentAt ?? null
});

const asBoolean = (value: unknown, fallback = false): boolean =>
  typeof value === 'boolean' ? value : fallback;

const rowToCampaign = (row: Row): CampaignRecord => ({
  id: asText(row.id),
  businessId: asText(row.business_id),
  name: asText(row.name),
  templateType: asText(row.template_type) as CampaignTemplateType,
  discountPercent: asNullableNumber(row.discount_percent),
  discountAmountCents: asNullableNumber(row.discount_amount_cents),
  currencyCode: asText(row.currency_code),
  targetServiceId: asNullableText(row.target_service_id),
  targetServiceName: asText(row.target_service_name),
  freeServiceId: asNullableText(row.free_service_id),
  freeServiceName: asText(row.free_service_name),
  happyHourStartTime: asNullableText(row.happy_hour_start_time),
  happyHourEndTime: asNullableText(row.happy_hour_end_time),
  offerName: asText(row.offer_name),
  originalPriceCents: asNullableNumber(row.original_price_cents),
  discountedPriceCents: asNullableNumber(row.discounted_price_cents),
  fillSlotDate: asNullableText(row.fill_slot_date),
  fillSlotTime: asNullableText(row.fill_slot_time),
  isAutoGenerated: asBoolean(row.is_auto_generated),
  smsBody: asText(row.sms_body),
  emailSubject: asText(row.email_subject),
  emailBodyText: asText(row.email_body_text),
  channel: asText(row.channel) as CampaignChannel,
  recipientSource: asText(row.recipient_source) as CampaignRecipientSource,
  status: asText(row.status) as CampaignStatus,
  recipientsTotal: asNumber(row.recipients_total),
  recipientsSent: asNumber(row.recipients_sent),
  recipientsFailed: asNumber(row.recipients_failed),
  recipientsSkipped: asNumber(row.recipients_skipped),
  linkOpensCount: asNumber(row.link_opens_count),
  bookingLink: asText(row.booking_link),
  createdAt: asTimestamp(row.created_at),
  updatedAt: asTimestamp(row.updated_at),
  sentAt: asNullableText(row.sent_at)
});

